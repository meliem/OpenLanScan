/// Lookup vendor by MAC address
pub fn lookup_vendor(mac_str: &str) -> Option<String> {
    // Normalize MAC address format (remove delimiters, convert to uppercase)
    let normalized: String = mac_str
        .chars()
        .filter(|c| c.is_ascii_alphanumeric())
        .collect::<String>()
        .to_uppercase();

    if normalized.len() < 6 {
        return None;
    }

    // Extract OUI (first 6 characters)
    let oui = &normalized[0..6];

    // Format as XX:XX:XX for lookup
    let oui_formatted = format!(
        "{}{}:{}{}:{}{}",
        &oui[0..1],
        &oui[1..2],
        &oui[2..3],
        &oui[3..4],
        &oui[4..5],
        &oui[5..6]
    );

    VENDOR_DB.get(oui_formatted.as_str()).map(|v| v.to_string())
}

